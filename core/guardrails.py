"""
core/guardrails.py — 双层护栏系统

职责：
  1. 无效问题拦截：检测用户提问是否与文档库有关，无关则直接拒答（零 Token 消耗）
  2. 输出格式约束：强制 LLM 回答时标注引用来源 [chunk_N]，避免无凭据编造
  3. 敏感词过滤：命中黑名单词 → 直接拒答

设计思路（面试可讲）：
  这是典型的 "输入护栏 + 输出护栏" 双层架构。
  - 输入护栏（敏感词 + 话题判断）：在调用 LLM 之前拦截，节省 Token
  - 输出护栏（引用标注验证）：在 LLM 返回之后校验，确保不胡说
  - 全部命中才放行 → 任一失败就返回拒答话术（Fail-Fast 原则）

用法：
  from core.guardrails import check_query, apply_input_guard
"""

import re
from typing import Tuple, Optional


# ============================================================
# 第一部分：敏感词黑名单
# ============================================================
# 命中这里任何一个词 → 直接拒答，不调 LLM
# 面试时可以说："这是最外层的安全护栏，零成本拦截"
SENSITIVE_KEYWORDS = [
    # 暴力/违法类
    "暴力", "色情", "赌博", "毒品", "枪支", "杀人", "炸弹",
    "贩毒", "走私", "诈骗", "黑客", "攻击网站",
    # 如需补充政治敏感词，在这里添加
    # "敏感词示例1", "敏感词示例2",
]


# ============================================================
# 第二部分：文档查询意图关键词（通用，不限文档主题）
# ============================================================
# 这些词表示用户"在问文档里的内容"，而不是闲聊。
# 不限制文档主题，只判断提问意图 —— 适用于任何领域的 RAG 系统。
#
# 面试时可以说：
# "用元问题关键词做意图识别，判断用户是在问文档内容还是在闲聊，
#  不依赖具体文档主题，适用于任何领域的 RAG 系统。"
DOC_QUERY_INTENT_KEYWORDS = [
    # 明确指向"文档内容"的信号词
    # 注意：不用"写""讲""说"等单字，太泛，容易误放行
    "文档", "记载", "提到", "里面", "内容", "原文",
    "章节", "第几章", "前面", "后面", "上文", "下文",
    "写了", "写到", "描写", "描述", "讲述","好的","可以","帮我看看",
    "剧本", "小说", "故事", "文本", "资料","行文","文章","行","嗯",
]


# ============================================================
# 第三部分：明显无关的话术（正则快速匹配）
# ============================================================
# 这些正则表达式匹配的是 100% 无关的问题类型。
# 比如 "今天天气怎么样"、"你叫什么名字" —— 任何文档都不会回答这些。
#
# 技术要点：用正则而不用 LLM 判断，因为正则匹配是 O(n) 的，
# 而调 LLM 要几百毫秒 + 消耗 Token。
OFF_TOPIC_PATTERNS = [
    # ---- 闲聊 / 询问 AI 身份 ----
    r"你是谁", r"你叫什么", r"你的名字",
    r"你是.*模型", r"你是.*AI", r"你有什么功能",
    # ---- 实时信息（文档里不会有）----
    r"今天天气", r"今天几号", r"现在几点",
    r"今天星期几", r"最新新闻", r"最近.*发生",
    # ---- 通用知识（非文档内容）----
    r"什么是人工智能", r"什么是机器学习",
    r"怎么做饭", r"怎么炒股", r"写.*代码",
    r"推荐.*电影", r"推荐.*书",
    # ---- 数学计算 ----
    r"计算.*等于", r"多少.*加.*多少",
]


# ============================================================
# 第四部分：拒答话术（统一管理，方便后续改文案）
# ============================================================
# 集中管理的好处：如果要改文案，只改这里就行。
REJECT_SENSITIVE  = "抱歉，此问题包含不当内容，我无法回答。"
REJECT_OFF_TOPIC  = "抱歉，此问题超出文档范围，我无法回答。请提出与文档内容相关的问题。"
REJECT_UNRELATED  = "抱歉，根据我的判断，您的问题与当前文档库内容无关，我无法回答。如有文档相关问题，欢迎提出。"


# ============================================================
# 第五部分：核心判断函数
# ============================================================

def contains_sensitive_words(query: str) -> bool:
    """
    检查问题是否包含敏感词。

    实现细节：
      - 大小写不敏感
      - 时间复杂度 O(n * m)，n=query长度，m=敏感词数量
      - 对于几十个敏感词的场景，性能完全够用

    参数:
        query: 用户原始问题
    返回:
        True  → 包含敏感词，应拒答
        False → 安全，放行
    """
    query_lower = query.lower()
    for word in SENSITIVE_KEYWORDS:
        if word.lower() in query_lower:
            return True
    return False


def is_off_topic(query: str) -> bool:
    """
    用正则快速判断问题是否"明显无关"。

    为什么用正则而不是 LLM：
      正则匹配是几微秒级别，LLM 调用是几百毫秒 + 消耗 Token。
      对于 "今天天气怎么样" 这种 100% 无意义的问题，
      完全没必要浪费一次 API 调用。

    参数:
        query: 用户原始问题
    返回:
        True  → 明显无关，建议拒答
        False → 不能确定，放行到下一步
    """
    for pattern in OFF_TOPIC_PATTERNS:
        if re.search(pattern, query, re.IGNORECASE):
            return True
    return False


def is_likely_doc_query(query: str) -> bool:
    """
    检查问题是否在"询问文档内容"（而非闲聊）。

    不是限制文档主题，而是判断用户的提问意图：
      - "文档里提到了谁？"    → 是文档查询意图 → 放行
      - "今天天气怎么样？"     → 是闲聊         → 拦截

    面试时可以说：
    "类似搜索引擎的 query understanding，用元问题关键词做轻量级意图识别。"

    参数:
        query: 用户原始问题
    返回:
        True  → 有文档查询意图，放行
        False → 没有，可能是闲聊
    """
    for keyword in DOC_QUERY_INTENT_KEYWORDS:
        if keyword in query:
            return True
    return False


# ============================================================
# 第六部分：护栏总入口（一条龙检查）
# ============================================================

def check_query(query: str) -> Tuple[bool, Optional[str]]:
    """
    护栏总入口：对用户问题执行三层检查。

    执行顺序采用短路逻辑（Short-Circuit）：
      1. 空问题检查       → 空输入直接拒
      2. 敏感词检查       → 命中则拒答，不浪费 LLM Token
      3. 明显无关话术检查  → 正则快速匹配，几微秒完成
      4. 超短问题检查     → ≤5字且无文档查询意图，大概率是闲聊
      5. 全部通过         → 放行，正常走 RAG 流程

    设计原则：
      "宁可误拦，不能放行有害内容；
       宁可误放，不能拦截用户的正当问题。"（偏安全的 Fail-Safe 策略）

    参数:
        query: 用户原始问题（可能是空字符串）

    返回:
        (是否放行, 拒答理由)
        - (True, None)    → 放行，继续检索+生成
        - (False, "xxx")  → 拦截，直接展示拒答话术，不调 LLM
    """
    # 第零关：空问题直接拒答，strip()删除字符串首尾所有「空白类字符」
    if not query or not query.strip():
        return False, REJECT_OFF_TOPIC

    # 第一关：敏感词（优先级最高）
    # 为什么放第一？安全问题必须零容忍
    if contains_sensitive_words(query):
        return False, REJECT_SENSITIVE

    # 第二关：明显无关话术
    # 正则匹配，极快，拦截掉 80% 的无效问题
    if is_off_topic(query):
        return False, REJECT_OFF_TOPIC

    # 第三关：超短问题 + 无文档查询意图
    # ≤3 字的问题通常是 "你好""在吗" 这种闲聊
    # 但如果包含文档查询意图关键词（如"看一下文档"），即使是短问题也放行
    if len(query.strip()) <= 3 and not is_likely_doc_query(query):
        return False, REJECT_OFF_TOPIC

    # 全部通过 → 放行，进入正常 RAG 检索流程
    return True, None


# ============================================================
# 第七部分：便捷集成函数
# ============================================================
# 让 base_rag.py 里的调用只需要一行代码

def apply_input_guard(query: str) -> Optional[str]:
    """
    对用户输入执行护栏检查，返回拒答话术或 None。

    在 interactive_qa() 里的用法（只需要加 3 行）：

        reject_msg = apply_input_guard(question)
        if reject_msg:
            print(f"回答：{reject_msg}")
            continue  # 不调 LLM，直接进入下一轮

    参数:
        query: 用户输入的问题
    返回:
        - 被拦截 → 返回拒答话术字符串
        - 放行   → 返回 None
    """
    passed, reject_reason = check_query(query)
    if not passed:
        return reject_reason
    return None


# ============================================================
# 第八部分：输出格式约束
# ============================================================
# 强制 LLM 在回答中标注引用来源，是输出护栏的核心。
# citation n.引文，引语
CITATION_INSTRUCTION = """
【引用标注规则 — 必须严格遵守】
1. 你的每一句回答，都必须标注信息来源：
   - 格式：[chunk_N] 文档名
   - N 是文档块编号，从 0 开始
   -如果某句话综合了多个文档块的信息，列出所有来源
2. 如果找不到对应的文档来源，标注：[无对应来源]
3. 如果整个问题超出文档范围，回答："抱歉，此问题超出文档范围，我无法回答。"
4. 禁止输出任何没有标注来源的事实陈述。
"""


