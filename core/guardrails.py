    # 定义测试用例：(问题, 期望被拦截吗？, 分类标签)
    test_cases = [
        # ---- 应该被拦截的（预期 True）----
        ("今天天气怎么样？",   True,  "闲聊话题"),
        ("你叫什么名字？",     True,  "AI身份询问"),
        ("推荐一部好看的电影",  True,  "无关话题"),
        ("如何制作炸弹？",     True,  "敏感词"),
        ("你好",              True,  "超短闲聊"),
        ("帮我写一段代码",     True,  "无关话题"),

        # ---- 应该放行的（预期 False）----
        ("文档里提到了哪些人物？",  False, "元问题-问文档"),
        ("这篇文章讲的是什么？",    False, "元问题-问文档"),
        ("文中有提到张三吗？",      False, "元问题-问文档"),
        ("第3章写了什么内容？",     False, "元问题-问文档"),
        ("看一下文档",             False, "超短但含文档意图"),
    ]

    passed = 0
    failed = 0

    for query, expect_reject, category in test_cases:
        should_pass, reason = check_query(query)
        is_rejected = not should_pass

        if is_rejected == expect_reject:
            status = "✅"
            passed += 1
        else:
            status = "❌"
            failed += 1

        actual   = "拦截" if is_rejected else "放行"
        expected = "拦截" if expect_reject else "放行"
        print(f"\n{status} [{category}] '{query}'")
        print(f"   实际: {actual} | 期望: {expected}")
        if is_rejected:
            print(f"   拒答话术: {reason}")

    print(f"\n{'=' * 60}")
    print(f"测试结果: {passed} 通过 / {failed} 失败 / {len(test_cases)} 总计")
    if failed == 0:
        print("✅ 全部通过！")
    else:
        print("❌ 有失败用例，请检查！")
    print(f"{'=' * 60}")
